# 11. Criar uma lista de 5 números e substituir o segundo elemento por um novo valor.

# Declaração de variáveis

lista = [1, 2, 3, 4, 5]
novo_valor = str(input('Digite um novo valor para o segundo elemento da lista: '))

# Apresentação de resultados

print('Lista original:', lista)
lista[1] = novo_valor
print('Lista com o segundo elemento substituído:', lista)